package com.verygoodsecurity.vgscollect.core

/**
 *  The set of common methods for HTTP.
 *
 *  @version 1.0.0
 */
enum class HTTPMethod {

    /**
     *  HTTP GET method
     */
    GET,

    /**
     *  HTTP POST method
     */
    POST,

    /**
     *  HTTP PUT method
     */
    PUT,

    /**
     *  HTTP PATCH method
     */
    PATCH,

    /**
     *  HTTP DELETE method
     */
    DELETE
}