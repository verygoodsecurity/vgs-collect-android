package com.verygoodsecurity.vgscollect.core

/**
 * The enum class represents a set of common methods for HTTP.
 */
enum class HTTPMethod {

    /**
     *  HTTP GET method
     */
    GET,

    /**
     *  HTTP POST method
     */
    POST,

    /**
     *  HTTP PUT method
     */
    PUT,

    /**
     *  HTTP PATCH method
     */
    PATCH,

    /**
     *  HTTP DELETE method
     */
    DELETE
}